const month = "mar"

switch (month){
    case "jan":
        console.log("january");
        break;
    case "feb":
        console.log("February");
        break;
    case "mar":
        console.log("March");
        break;
    case "apr":
        console.log("April");
        break;

    default:
        console.log("none");
        break;
}